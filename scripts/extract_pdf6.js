const ansKey = {
  1:1, 2:4, 3:1, 4:2, 5:4, 6:1, 7:3, 8:2, 9:1, 10:2,
  11:4, 12:2, 13:1, 14:1, 15:4, 16:4, 17:3, 18:1, 19:0, 20:2,
  21:2, 22:1, 23:2, 24:2, 25:1, 26:4, 27:1, 28:1, 29:3, 30:1
};

const questions = [];
let currentQ = null;

const lines = rawText.trim().split('\n');
let i = 0;

while (i < lines.length) {
  const line = lines[i].trim();
  const match = line.match(/^(\d+)\.\s*(.*)/);
  
  if (match) {
    if (currentQ) {
      questions.push(currentQ);
    }
    const qNum = parseInt(match[1]);
    currentQ = {
      id: `q_pdf6_${qNum.toString().padStart(2, '0')}`,
      domain: (qNum % 2 === 0) ? "math" : "verbal", // Very rough guess, subagent will fix if needed
      topic: "general",
      dif: 3,
      stem: match[2] + "\n",
      q: "",
      opts: [],
      ans: ansKey[qNum],
      exp: ""
    };
    i++;
    
    while (i < lines.length) {
      const nextLine = lines[i].trim();
      if (nextLine.match(/^\d+\.\s/)) {
        break; 
      }
      
      if (nextLine.match(/^[A-E]\)\s/)) {
        currentQ.opts.push(nextLine.replace(/^[A-E]\)\s*/, '').trim());
      } else if (nextLine !== "") {
        currentQ.stem += nextLine + "\n";
      }
      i++;
    }
  } else {
    i++;
  }
}

if (currentQ) {
  questions.push(currentQ);
}

questions.forEach(q => {
  q.stem = q.stem.trim();
  const parts = q.stem.split('\n');
  const lastPart = parts[parts.length - 1].trim();
  if (lastPart.includes('?')) {
     q.q = lastPart;
     parts.pop();
     q.stem = parts.join('\n').trim();
  }
});

console.log("Extracted:", questions.length, "questions");

fs.writeFileSync('temp_chunks/raw_pdf6.json', JSON.stringify(questions, null, 2));

const chunkSize = 5;
for (let j = 0; j < questions.length; j += chunkSize) {
  const chunk = questions.slice(j, j + chunkSize);
  const chunkIndex = Math.floor(j / chunkSize);
  fs.writeFileSync(`temp_chunks/chunk_6_${chunkIndex}.json`, JSON.stringify(chunk, null, 2));
}

